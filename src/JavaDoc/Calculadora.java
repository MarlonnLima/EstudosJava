public class Calculadora {
    /**
    * somar() este método é utilizado para somar dois números inteiros
    * @param numeroUm este é o primeiro parametro do método
    * @param numeroDois este é o segundo parametro do método
    * @return int o resultado deste método é a soma de dois números inteiros.
     */

    public int somar(int numeroUm, int numeroDois){
        return numeroUm + numeroDois;
    }
}
